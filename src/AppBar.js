function CardSetDropDown() {
  const [anchorEl, setAnchorEl] = React.useState(null);
  const [cardSets, setCardSets] = React.useState([]);
  const open = Boolean(anchorEl);
  const handleClick = (event) => {
    setAnchorEl(event.currentTarget);
  };
  const handleClose = () => {
    setAnchorEl(null);
  };

  useEffect(() => {
    API.fetchCardSets().then((data) => {
      setCardSets(data);
    });
  }, [])

  return (
    <div>
      <Button
        id="demo-customized-button"
        aria-controls={open ? 'demo-customized-menu' : undefined}
        aria-haspopup="true"
        aria-expanded={open ? 'true' : undefined}
        variant="contained"
        disableElevation
        onClick={handleClick}
        endIcon={<KeyboardArrowDownIcon />}
      >
        Card Sets
      </Button>
      <StyledMenu
        id="demo-customized-menu"
        MenuListProps={{
          'aria-labelledby': 'demo-customized-button',
        }}
        anchorEl={anchorEl}
        open={open}
        onClose={handleClose}
      >
      {cardSets.map((cardSet, idx) => (
        <MenuItem onClick={handleClose} key={"menu-"+idx} disableRipple>
          <Link href={"/"+cardSet.code}>{cardSet.code}</Link>
        </MenuItem>
      ))}
      </StyledMenu>
    </div>
  );
}

